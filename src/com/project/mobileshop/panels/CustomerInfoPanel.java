    private void editCustomerBtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editCustomerBtMouseClicked
        if (customerDetailsTable.getSelectedRow() == -1) {
            
            String content = "Select a customer to edit";
            JoptionMessageDialog dialog = new JoptionMessageDialog(null, true);
            dialog.getContent(content);
            return;
        }
        int customerRow = customerDetailsTable.getSelectedRow();

        String customerID = customerDetailsTable.getValueAt(customerRow, 0).toString();
        String customerName = customerDetailsTable.getValueAt(customerRow, 1).toString();
        int contactNo = Integer.parseInt(customerDetailsTable.getValueAt(customerRow, 2).toString());
        String NICno = customerDetailsTable.getValueAt(customerRow, 3).toString();

        CustomerDTO customerDTO = new CustomerDTO(customerID, customerName, contactNo, NICno);
        EditCustomerDialog customerDialog = new EditCustomerDialog(null, true);
        customerDialog.getInfo(customerDTO);

        customerDialog.setVisible(true);
    }//GEN-LAST:event_editCustomerBtMouseClicked

    private void customerIdSearchFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_customerIdSearchFieldActionPerformed

        if (customerIdSearchField.getText().equals("")) {
            LoadAllCustomers();
            return;
        }

        String customerId = customerIdSearchField.getText();
        CustomerDTO customerDTO = new CustomerDTO();
        customerDTO.setCustomerId(customerId);
        try {
            CustomerDTO search = CustomerController.searchFromID(customerDTO);
            if (search != null) {
                DefaultTableModel dtm = (DefaultTableModel) customerDetailsTable.getModel();
                dtm.setRowCount(0);
                Object[] row = {search.getCustomerId(), search.getCustomerName(), search.getContactNo(), search.getNICno()};
                dtm.addRow(row);
            }
        } catch (Exception ex) {
            Logger.getLogger(CustomerInfoPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_customerIdSearchFieldActionPerformed

