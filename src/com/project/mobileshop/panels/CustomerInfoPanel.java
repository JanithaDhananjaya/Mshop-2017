    public void LoadAllCustomers() {

        try {
            ArrayList<CustomerDTO> AllIds = CustomerController.LoadAll();
            if (AllIds != null) {

                DefaultTableModel dtm = (DefaultTableModel) customerDetailsTable.getModel();
                dtm.setRowCount(0);

                for (CustomerDTO c : AllIds) {
                    Object[] row = {c.getCustomerId(), c.getCustomerName(), c.getContactNo(), c.getNICno()};
                    dtm.addRow(row);
                }
            }

        } catch (SQLException ex) {
            Logger.getLogger(SupplierPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(CustomerInfoPanel.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

