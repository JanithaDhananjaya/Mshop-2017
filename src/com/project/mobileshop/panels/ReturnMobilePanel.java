    private void saveBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveBtnMouseClicked
        String returnId = returnIDTxt.getText();
        String IMEINo = IMEINoTxt.getText();
        String curDate = dateTxt.getText();
        String soldDate = soldDateTxt.getText();
        String warrenty = statusLbl.getText();
        String issue = issueTxt.getText();
        String customerId = customerIDTxt.getText();
        String supplierId = supplierIDTxt.getText();
        String soldInvId = soldInvNoTxt.getText();

        if (IMEINo.trim().isEmpty()) {
            return;
        }
        if (customerId.isEmpty()) {
            return;
        }
        if (issue.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mention the issue type");
            return;
        }

        ReturnMobileDTO returnMobileDTO = new ReturnMobileDTO(returnId, IMEINo, curDate, soldDate, warrenty, issue, customerId, supplierId, soldInvId);

        try {
            boolean isAdded = ReturnMobileController.addReturnMobile(returnMobileDTO);
            if (isAdded) {
                JOptionPane.showMessageDialog(this, "Successfully Added");
            } else {
                JOptionPane.showMessageDialog(this, "Error");
            }
        } catch (Exception ex) {
            Logger.getLogger(ReturnMobilePanel.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_saveBtnMouseClicked

