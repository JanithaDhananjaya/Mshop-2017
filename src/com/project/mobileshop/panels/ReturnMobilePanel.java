    private void IMEINoTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IMEINoTxtActionPerformed
        String IMEINo = IMEINoTxt.getText();
        try {
            CustomDTO returnMobileDetails = ReturnMobileController.getReturnMobileDetails(IMEINo);
            if (returnMobileDetails == null) {

                String content = "Invalid IMEI No input";
                JoptionMessageDialog dialog = new JoptionMessageDialog(null, true);
                dialog.getContent(content);
                return;
            }

            int warranty = returnMobileDetails.getWarrentyPeriod();

            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String soldDate = returnMobileDetails.getSoldInvoiceDate();
            String curDate = dateTxt.getText();

            try {
                Date date1 = dateFormat.parse(soldDate);
                Date date2 = dateFormat.parse(curDate);
                long diff = date2.getTime() - date1.getTime();
                System.out.println("Days: " + TimeUnit.DAYS.convert(diff, TimeUnit.MILLISECONDS));
                long months = TimeUnit.DAYS.convert(diff, TimeUnit.MILLISECONDS) / 30;
                System.out.println("Months :" + months);

                if (months >= warranty) {
                    statusLbl.setVisible(true);
                    statusLbl.setText("Expired");
                } else {
                    statusLbl.setVisible(true);
                    statusLbl.setText("Valid");
                }

            } catch (ParseException e) {
                e.printStackTrace();
            }

            customerIDTxt.setText(returnMobileDetails.getCustomerId());
            customerNameTxt.setText(returnMobileDetails.getCustomerName());
            supplierIDTxt.setText(returnMobileDetails.getSupplierId());
            supplierNameTxt.setText(returnMobileDetails.getSupplierName());
            soldDateTxt.setText(returnMobileDetails.getSoldInvoiceDate());
            soldInvNoTxt.setText(returnMobileDetails.getSoldInvoiceNo());
            warrentyTxt.setText(String.valueOf(returnMobileDetails.getWarrentyPeriod()));

        } catch (Exception ex) {
            Logger.getLogger(ReturnMobilePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_IMEINoTxtActionPerformed

