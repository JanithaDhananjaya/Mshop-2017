    public void setBatchNos(BatchDTO batchDTO, String modelName) throws Exception {
        System.out.println("model No in dialog box  " + batchDTO.getModelNo());
        ArrayList<BatchDTO> all = BatchController.searchBatch(batchDTO);
        if (all != null) {
            for (BatchDTO a : all) {
                String batch = a.getBatchNo();

                modelNmeTxt.setText(modelName);
                batchNoComboBox.addItem(batch);
            }
        }

    }

