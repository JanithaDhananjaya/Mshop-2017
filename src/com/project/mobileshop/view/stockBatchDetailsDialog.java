    private void batchNoComboBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_batchNoComboBoxItemStateChanged
        String batchNo = batchNoComboBox.getSelectedItem().toString();
        BatchDTO batchDTO = new BatchDTO();
        batchDTO.setBatchNo(batchNo);
        try {
            BatchDTO b = BatchController.getSelectedBatchDetails(batchNo);
            if (b != null) {
                spendAmountTxt.setText(String.valueOf(b.getAmount()));
                deliverdPriceTxt.setText(String.valueOf(b.getOriginalPrice()));
                SellingPriceTxt.setText(String.valueOf(b.getSellingPrice()));
                qtyOnHandTxt.setText(String.valueOf(b.getQty()));

            } else {
                JOptionPane.showMessageDialog(this, "No suitable details for this batch no");
            }

        } catch (Exception ex) {
            Logger.getLogger(stockBatchDetailsDialog.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_batchNoComboBoxItemStateChanged

