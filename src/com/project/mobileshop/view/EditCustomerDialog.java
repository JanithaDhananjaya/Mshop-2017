    private void saveChangesBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveChangesBtnMouseClicked
        String custId = custmerIdTxt.getText();
        String custName = customerNameTxt.getText();
        int contNo=Integer.parseInt(contactNoTxt.getText());
        String NICNo = NICnoTxt.getText();
        CustomerDTO customerDTO=new CustomerDTO(custId, custName, contNo, NICNo);
        try {
            boolean isUpdated = CustomerController.update(customerDTO);
            if(isUpdated){
                //JOptionPane.showMessageDialog(this, "Saved Successfully");
                String content = "Saved Successfully";
                JoptionMessageDialog dialog = new JoptionMessageDialog(null, true);
                dialog.getContent(content);
            }else{
               //JOptionPane.showMessageDialog(this, "Error");
                String content = "Not Saved";
                JoptionMessageDialog dialog = new JoptionMessageDialog(null, true);
                dialog.getContent(content);
            }
        } catch (Exception ex) {
            Logger.getLogger(EditCustomerDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_saveChangesBtnMouseClicked

