    private void contactNoTxtKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_contactNoTxtKeyTyped
          char keyChar = evt.getKeyChar();
        String IMEI = contactNoTxt.getText();
        if (!Character.isDigit(keyChar)) {
            evt.consume();
        }
        if(IMEI.length()>9){
             evt.consume();
        }
    }//GEN-LAST:event_contactNoTxtKeyTyped

