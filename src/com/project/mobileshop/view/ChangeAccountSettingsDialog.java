    private void curPasswordTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_curPasswordTxtActionPerformed
        String psw = new String(curPasswordTxt.getPassword());
        UserDTO dTO = new UserDTO();
        dTO.setPassword(psw);
        try {
            UserDTO search = UserController.search(dTO);
            System.out.println("pass :" + dTO.getPassword());
            if (search.getPassword().equals(psw)) {
                URL resource = this.getClass().getResource("/com/project/mobileshop/images/Images/Checked_50px.png");
                ImageIcon imageIcon = new ImageIcon(resource);
                checkedLbl.setIcon(imageIcon);
                newPasswordTxt.requestFocus();
            } else {
                URL resource = this.getClass().getResource("/com/project/mobileshop/images/Images/Cancel_48px.png");
                ImageIcon imageIcon = new ImageIcon(resource);
                checkedLbl.setIcon(imageIcon);
                curPasswordTxt.selectAll();
            }
        } catch (Exception ex) {
            Logger.getLogger(ChangeAccountSettingsDialog.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_curPasswordTxtActionPerformed

