    private void itemDetailsBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_itemDetailsBtnMouseClicked
        if (viewAllGrnTable.getSelectedRow() == -1) {
            //JOptionPane.showMessageDialog(this, "Select a GRN No");
            String content = "Select a GRN No";
            JoptionMessageDialog dialog = new JoptionMessageDialog(null, true);
            dialog.getContent(content);
            return;
        }

        int selectedRow = viewAllGrnTable.getSelectedRow();
        String GRNno = viewAllGrnTable.getValueAt(selectedRow, 0).toString();
        String Date = viewAllGrnTable.getValueAt(selectedRow, 1).toString();
        String supplierID = viewAllGrnTable.getValueAt(selectedRow, 2).toString();
        double Amount = Double.parseDouble(viewAllGrnTable.getValueAt(selectedRow, 3).toString());

        GRNDTO grndto = new GRNDTO(GRNno, supplierID, Date, Amount);

        GRNItemDetailsDailog d = new GRNItemDetailsDailog(null, true);
        d.getGRN(grndto);
        d.setVisible(true);
    }//GEN-LAST:event_itemDetailsBtnMouseClicked

