    public void LoadAllGRNs() {
        try {
            ArrayList<GRNDTO> grnList = GRNController.getAllGRNs();
            DefaultTableModel dtm = (DefaultTableModel) viewAllGrnTable.getModel();
            dtm.setRowCount(0);
            for (GRNDTO g : grnList) {
                Object[] row = {g.getGRNId(), g.getGRNDate(), g.getSupplierId(), g.getTotalAmount()};
                dtm.addRow(row);
            }
        } catch (Exception ex) {
            Logger.getLogger(ViewGRNs.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

